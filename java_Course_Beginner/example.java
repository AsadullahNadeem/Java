public class example {
    public static void main(String[] args) {

        ///INT VALUES
       int a = 845;
       int b = 8989839;
      System.out.println(a*b);

      ///BOOLEAN VALUES
        boolean good;
        boolean hhh;
        System.out.println(good = true);
        System.out.println(hhh = false);

        ///THREE DIGIT VALUES

        int m = 496898968;
        int n = 348745895;
        int c = 894778347;
        System.out.println(n+m*c);

        /// int five digit values
        int hh = 9904023;
        int jj = 90909090;
        int kk = 89045808;
        int jjl = 990909095;
        int mmm= 405904950;
        System.out.println(hh + jj * kk / jjl - mmm);
    }
}
